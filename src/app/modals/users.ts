import { model, models, Schema } from "mongoose";

const UserSchema = new Schema({
    firstname: {
        type: String,
        required: true,
        trim: true
    },
    lastname: {
        type: String,
        required: true,
        trim: true
    },
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true,
        minlength: 6  // Fixed typo from 'minlenght'
    }
}, {
    timestamps: true,
    versionKey: false
});

export const User = models.User || model('User', UserSchema);